#include <stdlib.h>
#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	min;
	size_t	max;
	size_t	len;

	if (!s)
		return (NULL);
	min = 0;
	while (s[min] && ft_iswhitespace(s[min]) == 1)
		min++;
	max = ft_strlen(s);
	while (min < max && ft_iswhitespace(s[max - 1]) == 1)
		max--;
	if (min == max)
		return (ft_strnew(1));
	len = max - min;
	return (ft_strsub(s, min, len));
}
